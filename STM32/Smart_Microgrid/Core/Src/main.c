void send_to_esp8266(const char* message) {
  HAL_UART_Transmit(&huart6, (uint8_t*)message, strlen(message), HAL_MAX_DELAY);
}
uint16_t read_adc_channel(uint32_t channel) {
   ADC_ChannelConfTypeDef sConfig = {0};
   sConfig.Channel = channel;
   sConfig.Rank = 1;
   sConfig.SamplingTime = ADC_SAMPLETIME_3CYCLES;
   HAL_ADC_ConfigChannel(&hadc1, &sConfig);
   HAL_ADC_Start(&hadc1);
   HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
   uint16_t value = HAL_ADC_GetValue(&hadc1);
   HAL_ADC_Stop(&hadc1);
   return value;
}
void send_current_and_voltage_to_esp8266(uint16_t current, uint16_t voltage) {
   snprintf(buffer, sizeof(buffer), "{\"Current\":%d, \"Voltage\":%d}\n", current, voltage);
   send_to_esp8266(buffer);
}

